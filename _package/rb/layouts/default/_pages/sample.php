	<h1>레이아웃 자체 페이지 추가안내</h1>

	<p>
	킴스큐는 자체적으로 페이지추가 기능을 제공하고 있습니다. <i>(상단 관리바 &gt; 페이지)</i><br />
	그러나 종종 레이아웃 자체에서 간단히 파일만 추가해서 페이지를 구성할 필요가 생깁니다.<br />
	이런 경우 레이아웃내에 페이지용 파일을 업로드하면 간단히 페이지를 추가할 수 있습니다.<br />
	<br />
	<br />

	<span class="b">이 레이아웃에서 사용할 레이아웃 전용 페이지를 추가하는 방법!?</span><br />
	</p>
	
	<div class="olbox">
	<ol>
	<li>추가하려는 페이지 파일을 만듭니다.<i>파일의 확장자는 .php 로 합니다<br/>(보기) 회사소개 페이지를 구성할 경우 : company.php</i></li>
	<li>CSS가 필요할 경우 CSS파일을 만듭니다.<i>페이지 파일명과 동일하게 만듭니다.<br />(보기) company.css</i></li>
	<li>php파일과 css파일을 원하는 형식으로 구성(편집)합니다.</li>
	<li>편집한 파일을 이 레이아웃의 폴더내 _pages 안에 업로드합니다.</li>
	<li>페이지를 호출합니다.<br /><i>페이지 호출방법 : /rb/?r=사이트코드&amp;_themePage=파일명(확장자제외)<br />(변수) &lt;?php echo $g['s']?&gt;/?r=&lt;?php echo $r?&gt;&amp;_themePage=파일명(확장자제외)<br />하나의 사이트만 생성해서 운영할 경우 사이트코드는 생략가능합니다.</i></li>
	<li>이 샘플페이지를 참고하세요.<i>/레이아웃폴더/_pages/sample.php 와 sample.css</i></li>
	</ol>
